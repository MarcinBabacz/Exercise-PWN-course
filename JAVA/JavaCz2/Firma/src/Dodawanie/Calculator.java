package Dodawanie;

public class Calculator {

	void add( int a, int b){
		System.out.println("Wynik to: " + (a+b));
	}
	void add( int a, int b , int c){
		System.out.println("Wynik to: " + (a+b+c));
	}
	void sub( int a, int b){
		System.out.println("Wynik to: " + (a-b));
	}
	void sub( int a, int b , int c){
		System.out.println("Wynik to: " + (a-b-c));
	}
}
